public class Swapping{
    public static void main (String args[]){
       //before swapping
        int a = 12;
        int b = 43;
        System.out.println("Before swapping the values are " +a+ " and "+b);
        
        // after swapping
         int t=a;
         a=b;
         b=t;
         
         System.out.println("After swapping the values are " +a+ " and "+b);
        
}
}